# Data Types in Python
# Common data types include:
# 1. Integer (int)
# 2. Float (float)
# 3. String (str)
# 4. Boolean (bool)
# 5. List (list)
# 6. Tuple (tuple)  

# Example of different data types
age = 25                      # Integer 
height = 5.9                  # Float
name = "Shreenibas"          # String
is_student = True            # Boolean
grades = [90, 85, 88]       # List
coordinates = (10.0, 20.0)   # Tuple
# Print the data types
print("Age:", age, "Type:", type(age))
print("Height:", height, "Type:", type(height))

print("Name:", name, "Type:", type(name))
print("Is Student:", is_student, "Type:", type(is_student))
print("Grades:", grades, "Type:", type(grades))
print("Coordinates:", coordinates, "Type:", type(coordinates))
# You can run this code to see the output and understand the data types in Python.

